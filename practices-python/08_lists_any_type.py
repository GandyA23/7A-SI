# Lista con diferentes tipos de datos 
lista = [1, 2.5, 'cadena', [5,6], 4]

print(lista[0])     # 1
print(lista[1])     # 2.5
print(lista[2])     # cadena
print(lista[3])     # [5, 6]
print(lista[3][0])  # 5
print(lista[3][1])  # 6
print(lista[1:3])   # [2.5, 'cadena']
print(lista[1:6])   # [2.5, cadena, [5, 6], 4]

